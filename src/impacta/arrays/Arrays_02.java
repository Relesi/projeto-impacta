package impacta.arrays;

public class Arrays_02 {
    public static void main(String[] args) {
        String[] nomes = new String[5];
        nomes[0] = "Joao";
        nomes[1] = "Maria";
        nomes[2] = "Jose";
        nomes[3] = "Jon";
        nomes[4] = "Mary";


        for (int i = 0; i < nomes.length; i++){
            System.out.println(nomes[i]);
        }
    }
}
